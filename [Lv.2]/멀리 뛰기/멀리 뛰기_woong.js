function solution(n) {
    let temp1 = 1;
    let temp2 = 1;
    let result = 0;
//   질문하기 보고 1번 테스트 케이스 실패한 사람 힌트를 봄
    if(n == 1){
        return n

    }
    for(let i = 1; i < n; i++){
        result = temp1 + temp2;
        temp1 = temp2;
        temp2 = result % 1234567; // ???? return에 붙여준거랑 차이가 너무 많이 나는데 1번 테스트 케이스 통과가 안된다
    }
    
    return result % 1234567;
}
